
;;; source file for CC65 "remake" compiler
	.include "soundbs.mac"
	.global _musicptr
	.segment "TUNE0_RODATA"
_musicptr:
	.word CHANNEL_0, CHANNEL_1, CHANNEL_2, CHANNEL_3


PATTERN_LENGTH   .set  2560

CHANNEL_0:
CHAN_STOP

CHANNEL_1:
	PATTERN ENVIROS
	PATTERN PATTERN_01
CHAN_STOP

CHANNEL_2:
	PATTERN ENVIROS
	PATTERN PATTERN_02
CHAN_STOP

CHANNEL_3:
	PATTERN ENVIROS
	PATTERN PATTERN_03
CHAN_STOP

ENVIROS:
	SETPLAYERFREQ $9D,129
	DEF_VOLENV 1,TREMOLO_1
	DEF_VOLENV 2,TREMOLO_2
	DEF_FRQENV 2,VIBRATO_2
	DEF_VOLENV 3,TREMOLO_3
	DEF_VOLENV 4,TREMOLO_4
	DEF_VOLENV 5,TREMOLO_5
	DEF_VOLENV 6,TREMOLO_6
	DEF_VOLENV 7,TREMOLO_7
	DEF_FRQENV 7,VIBRATO_7
	DELAY 3
RETURN

TREMOLO_1:
.byte $04,$05,$02,$0A,$15,$00,$0B,$FF,$0B,$00,$01,$FE
TREMOLO_2:
.byte $02,$02,$05,$00,$01,$F8
VIBRATO_2:
.byte $01,$02,$06,$05,$06,$FB
TREMOLO_3:
.byte $02,$06,$15,$01,$06,$00,$02,$FC,$10,$00,$01,$FC,$02,$05
TREMOLO_4:
.byte $02,$02,$10,$00,$01,$EC
TREMOLO_5:
.byte $01,$05,$02,$04,$06,$00,$02,$FC,$10,$00,$01,$FC
TREMOLO_6:
.byte $02,$02,$10,$00,$01,$EC
TREMOLO_7:
.byte $03,$04,$01,$28,$06,$00,$01,$FE,$02,$00
VIBRATO_7:
.byte $01,$02,$01,$05,$01,$FF


PATTERN_01:
	INSTR2 $40,$00,$7b,33,127
	SET_VOLENV 4
	PLAY2 173,$58,255
RETURN


PATTERN_02:
	INSTR2 $16,$00,$0d,24,127
	SET_VOLENV 5
	PLAY2 129,$78,255
RETURN


PATTERN_03:
	INSTR2 $40,$00,$7b,127,127
	SET_VOLENV 6
	PLAY2 86,$58,40
	INSTR2 $e2,$00,$35,1,127
	SET_VOLENV 7
	SET_FRQENV 7
	PLAY2 52,$58,255
RETURN


